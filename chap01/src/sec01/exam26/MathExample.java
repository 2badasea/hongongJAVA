package sec01.exam26;

// Math 수학 메소드
public class MathExample {

	public static void main(String[] args) {
		// Math클래스가 제공하는 메소드들은 모두 static이므로 바로 접근 가능. 
		int v1 = Math.abs(-5);
		double v2 = Math.abs(-3.14);
		System.out.println("v1의 값: " + v1);
		System.out.println("v2s의 값: " + v2);
	}
}
